<p style="color:red">Words per minute</p>
Further information: Words per minute
Words per minute (WPM) is a measure of typing speed, commonly used in recruitment. For the purposes of WPM measurement a word is standardized to five characters or keystrokes. Therefore, "brown" counts as one word, but "mozzarella" counts as two.

The benefits of a standardized measurement of input speed are that it enables comparison across language and hardware boundaries. The speed of an Afrikaans-speaking operator in Cape Town can be compared with a French-speaking operator in Paris.

Today, even Written Chinese can be typed very quickly using the combination of a software prediction system and by typing their sounds in Pinyin.[6] Such prediction software even allows typing short-hand forms while producing complete characters. For example, the phrase "nǐ chīle ma" (你吃了吗) meaning "Have you eaten yet?" can be typed with just 4 strokes: "nclm".
<hr><p style ="color:green">
Alphanumeric entry</p>
In one study of average computer users, the average rate for transcription was 33 words per minute, and 19 words per minute for composition.[7] In the same study, when the group was divided into "fast", "moderate" and "slow" groups, the average speeds were 40 wpm, 35 wpm, and 23 wpm respectively. An average professional typist reaches 50 to 80 wpm, while some positions can require 80 to 95 wpm (usually the minimum required for dispatch positions and other typing jobs), and some advanced typists work at speeds above 120 wpm.[8][9] Two-finger typists, sometimes also referred to as "hunt and peck" typists, commonly reach sustained speeds of about 37 wpm for memorized text and 27 wpm when copying text, but in bursts may be able to reach speeds of 60 to 70 wpm.[10] From the 1920s through the 1970s, typing speed (along with shorthand speed) was an important secretarial qualification and typing contests were popular and often publicized by typewriter companies as promotional tools.

A less common measure of the speed of a typist, CPM is used to identify the number of characters typed per minute. This is a common measurement for typing programs, or typing tutors, as it can give a more accurate measure of a person's typing speed without having to type for a prolonged period of time. The common conversion factor between WPM and CPM is 5. It is also used occasionally for associating the speed of a reader with the amount they have read. CPM has also been applied to 20th century printers, but modern faster printers more commonly use PPM (pages per minute).

The fastest typing speed ever, 216 words per minute, was achieved by Stella Pajunas-Garnand from Chicago in 1946 in one minute on an IBM electric[11][12][13][14] using the QWERTY keyboard layout.[15][16] As of 2005, writer Barbara Blackburn was the fastest English language typist in the world, according to The Guinness Book of World Records. Using the Dvorak keyboard layout, she had maintained 150 wpm for 50 minutes, and 170 wpm for shorter periods, with a peak speed of 212 wpm. Blackburn, who failed her QWERTY typing class in high school, first encountered the Dvorak layout in 1938, quickly learned to achieve very high speeds, and occasionally toured giving speed-typing demonstrations during her secretarial career. She appeared on Late Night with David Letterman on January 24, 1985, but felt that Letterman made a spectacle of her.[17]

The recent emergence of several competitive typing websites has allowed fast typists on computer keyboards to emerge along with new records, though many of these are unverifiable. Some notable, verified records include 255 wpm on a one-minute, random-word test by a user under the username slekap and occasionally bailey,[18] 213 wpm on a 1-hour, random-word test by Joshua Hu,[19] 221 wpm average on 10 random quotes by Joshua Hu,[20] and first place in the 2020 Ultimate Typing Championship by Anthony Ermollin based on an average of 180.88 wpm on texts of various lengths.[21][22] These three people are the most commonly cited fastest typists in online typing communities.[citation needed] All of their records were set on the QWERTY keyboard layout.

Using a personalized interface, physicist Stephen Hawking, who suffered from amyotrophic lateral sclerosis, managed to type 15 wpm with a switch and adapted software created by Walt Woltosz. Due to a slowdown of his motor skills, his interface was upgraded with an infrared camera that detected "twitches in the cheek muscle under the eye."[23] His typing speed decreased to approximately one word per minute in the later part of his life.[24]
